import Foundation

// MARK: - Analysis Output

/// Structured analysis output produced by the InterpretationEngine.
/// This is the source of truth for workout insights - all data is calculated locally.
struct AnalysisOutput: Codable, Equatable {

    // MARK: - Core Analysis

    /// Overall sentiment of the workout analysis
    let sentiment: AnalysisSentiment

    /// Confidence score (0.0 - 1.0) based on data quality
    let confidence: Double

    /// Timestamp when analysis was generated
    let generatedAt: Date

    // MARK: - Workout Metrics

    /// Key metrics extracted from the workout
    let metrics: WorkoutMetrics

    // MARK: - Insights

    /// Individual insight points with structured data
    let insights: [StructuredInsight]

    // MARK: - Recommendations

    /// Actionable recommendations based on the analysis
    let recommendations: [StructuredRecommendation]

    // MARK: - Pattern Analysis

    /// Patterns detected from workout history
    let patterns: [DetectedPattern]

    // MARK: - Recovery Context

    /// Recovery-related context if available
    let recoveryContext: RecoveryContext?

    // MARK: - Nutrition Context

    /// Nutrition/hydration context if available
    let nutritionContext: NutritionContext?

    // MARK: - Data Completeness

    /// Information about data quality/completeness
    let dataCompleteness: DataCompleteness?

    // MARK: - Generated Text (Local Rules)

    /// Summary text generated by local rules
    let localSummary: String

    /// Detailed interpretation from local rules
    let localInterpretation: String

    /// Recommendation text from local rules
    let localRecommendation: String?

    // MARK: - AI-Enhanced Text (Future)

    /// AI-rewritten summary (nil until OpenAI integration)
    var aiEnhancedSummary: String?

    /// AI-rewritten interpretation (nil until OpenAI integration)
    var aiEnhancedInterpretation: String?

    /// Whether AI enhancement is available
    var hasAIEnhancement: Bool {
        aiEnhancedSummary != nil || aiEnhancedInterpretation != nil
    }

    // MARK: - Display Helpers

    /// Returns the best available summary (AI-enhanced if available, otherwise local)
    var displaySummary: String {
        aiEnhancedSummary ?? localSummary
    }

    /// Returns the best available interpretation
    var displayInterpretation: String {
        aiEnhancedInterpretation ?? localInterpretation
    }
}

// MARK: - Analysis Sentiment

enum AnalysisSentiment: String, Codable, CaseIterable {
    case positive = "positive"
    case neutral = "neutral"
    case caution = "caution"

    var displayName: String {
        switch self {
        case .positive: return "Great workout"
        case .neutral: return "Solid effort"
        case .caution: return "Take note"
        }
    }
}

// MARK: - Workout Metrics

struct WorkoutMetrics: Codable, Equatable {
    let duration: TimeInterval
    let calories: Int
    let averageHeartRate: Int?
    let maxHeartRate: Int?
    let distance: Double?
    let intensity: String
    let workoutType: String

    /// Computed efficiency score (0-100)
    var efficiencyScore: Int? {
        guard let avgHR = averageHeartRate, avgHR > 0 else { return nil }
        // Simple efficiency: lower HR for similar output = better
        let baseScore = 70
        let hrAdjustment = avgHR < 140 ? 15 : (avgHR > 170 ? -15 : 0)
        return min(100, max(0, baseScore + hrAdjustment))
    }
}

// MARK: - Structured Insight

struct StructuredInsight: Codable, Equatable, Identifiable {
    let id: UUID
    let category: InsightCategory
    let sentiment: AnalysisSentiment
    let weight: Double // 0.0 - 1.0, higher = more important
    let icon: String
    let title: String
    let detail: String
    let dataPoints: [String: String]? // Raw data backing this insight

    init(
        id: UUID = UUID(),
        category: InsightCategory,
        sentiment: AnalysisSentiment,
        weight: Double,
        icon: String,
        title: String,
        detail: String,
        dataPoints: [String: String]? = nil
    ) {
        self.id = id
        self.category = category
        self.sentiment = sentiment
        self.weight = weight
        self.icon = icon
        self.title = title
        self.detail = detail
        self.dataPoints = dataPoints
    }
}

enum InsightCategory: String, Codable, CaseIterable {
    case heartRate = "heart_rate"
    case duration = "duration"
    case intensity = "intensity"
    case recovery = "recovery"
    case pattern = "pattern"
    case sleep = "sleep"
    case hydration = "hydration"
    case progress = "progress"
}

// MARK: - Structured Recommendation

struct StructuredRecommendation: Codable, Equatable, Identifiable {
    let id: UUID
    let priority: RecommendationPriority
    let category: RecommendationCategory
    let title: String
    let detail: String
    let actionable: Bool

    init(
        id: UUID = UUID(),
        priority: RecommendationPriority,
        category: RecommendationCategory,
        title: String,
        detail: String,
        actionable: Bool = true
    ) {
        self.id = id
        self.priority = priority
        self.category = category
        self.title = title
        self.detail = detail
        self.actionable = actionable
    }
}

enum RecommendationPriority: String, Codable, CaseIterable {
    case high = "high"
    case medium = "medium"
    case low = "low"
}

enum RecommendationCategory: String, Codable, CaseIterable {
    case nextWorkout = "next_workout"
    case recovery = "recovery"
    case nutrition = "nutrition"
    case sleep = "sleep"
    case technique = "technique"
    case general = "general"
}

// MARK: - Detected Pattern

